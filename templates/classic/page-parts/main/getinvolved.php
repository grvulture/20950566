<p style="margin-bottom: 10px;">What you see here can be described in 3 small sentences:<br />
Klipsam is a personal project. Klipsam was created from scratch. Klipsam has great potential!</p>
<p style="margin-bottom: 10px;">I created Klipsam all by myself, and I really believe in its potential. 
I do believe it can reach millions of users who are looking for an easy way to share stuff, with an easy way to organize it,
retrieve it, archive it, and socialize on it.</p>
<p style="margin-bottom: 10px;">Klipsam is still far away from delivering the services I dream of, and that's only because I am the only one actively developing it.
However, the possibilities are endless. For Klipsam to reach its full potential, it needs marketing, sponsors, and investors to finance its development and operation.</p>
<p style="margin-bottom: 10px;">Development includes big improvements of current functionality, UI & UX, and the creation of new functionality such as photo-tagging, klip-tagging, etc.</p>
<p style="margin-bottom: 10px;">I am full of ideas about Klipsam and how to make it more appealing with new, innovative functionality that will engage its users, however I lack the funds and the time to deliver all this within a reasonable timeframe.</p>
<p style="margin-bottom: 10px;">And that's where the operation costs come in play, and why I need someone who will believe in Klipsam as much as I do. Who will share the dream with me!</p>
